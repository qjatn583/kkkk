public class Calculator {
	//정사각형의 넓이
double areaRectangle(double width) {//double타입의 areaRectangle를 선언해주고 매개변수인 width를 대입
	return width*width;//width*width값을 리턴
}
//직사각형의 넓이
double areaRectangle(double width,double height) {//double타입의 areaRectangle를 선언해주고 width,height의 매개변수 선언
	return width*height;//width*height값 리턴
}
}
